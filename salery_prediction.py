import pandas as pd

salary=pd.read_csv('https://github.com/YBI-Foundation/Dataset/raw/main/Salary%20Data.csv')

salary.head()

salary.info()

salary.describe()

salary.columns

salary.shape

y=salary['Salary']#dependent variable
x=salary[['Experience Years']]#independent

x.shape

y.shape

from sklearn.model_selection import train_test_split

x_train,x_test,y_train,y_test=train_test_split(x,y,train_size=0.7,random_state=2529)
x_train

x_train.shape,x_test.shape

y_train.shape,y_test.shape

from sklearn.linear_model import LinearRegression

model=LinearRegression()

model.fit(x_train,y_train)

model.intercept_

model.coef_

y_predict=model.predict(x_test)

from sklearn.metrics import mean_absolute_percentage_error
error=mean_absolute_percentage_error(y_test,y_predict)
print(error)

new_salary=pd.DataFrame([[7.6]],['Experience Years'])

new_prediction=model.predict(new_salary)

print(new_prediction)